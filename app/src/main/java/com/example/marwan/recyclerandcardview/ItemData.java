// Blueprint for the movies objects

public class ItemData {
    private String head;
    private String desc;

    public ItemData(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
